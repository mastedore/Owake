#include "state.hpp"
#include <avr/pgmspace.h>

constexpr char endline = 0x0b;
constexpr char pause = 0x0a;
constexpr char stop = 0x0c;

static void typeEffect(const char *text)
{
	int col = 0, row = 0;
	int textIndex = 0;

	// Type out the text
	while (text[textIndex] != stop)
	{
		char c = text[textIndex++];

		if (c == endline)
		{
			row++;
			col = 0;
			lcd.writePosition(col, row);
		}
		else if (c == pause)
		{
			delay(1500);
			// Delete the text
			while (col > 0 || row > 0)
			{
				if (col > 0)
				{
					col--;
				}
				else if (row > 0)
				{
					row--;
					col = 15; // Assuming 16-char wide display
				}

				lcd.writePosition(col, row);
				lcd.writeChar(' ');
				delay(66);
			}
		}
		else
		{
			lcd.writePosition(col, row);
			lcd.writeChar(c);
			col++;
			delay(84);
		}
	}

	delay(2000);

	lcd.writePosition(0, 0);
}

static void print(char p)
{
	lcd.writeChar(p);
}

